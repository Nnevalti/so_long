#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t			i;
	unsigned char	*str;

	i = 0;
	str = (unsigned char *)s;
	while (i < n && (*str || c == '\0'))
	{
		if (*str == (unsigned char)c)
			return (str);
		i++;
		str++;
	}
	return (NULL);
}
